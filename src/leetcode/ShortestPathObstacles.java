    public int shortestPath1(int[][] grid, int k) {
        int r = grid.length;
        int c = grid[0].length;
        //a node in the below 2-d array states, how much max steps has a vehicle would have driven past this post
        //If I encounter maxDistDrivble[i][j] whose value is more than what currently I have, then I would not date
        //to pass this node as I clearly know that some other path would have a much higher range compared to what I can
        //obtain with my current capacity. Only if my capacity is more than the past record associated with the node, then I would
        //take this path
        int[][] maxDistDrivble = new int[r][c];

        for (int[] row : maxDistDrivble)
            Arrays.fill(row, -1);

        int[][] directions = {{1, 0}, {-1, 0}, {0, 1}, {0, -1}};
        Queue<int[]> queue = new LinkedList<>();

        //0 - x, 0- y, 0- total steps so far taken, k- total steps I am allowed to take
        queue.offer(new int[]{0, 0, 0, k});

        while (!queue.isEmpty()) {

            int[] curr = queue.poll();
            int x = curr[0];
            int y = curr[1];
            if (x == r - 1 && y == c - 1) return curr[2];
            if (grid[x][y] == 1) {
                curr[3]--;
            }

            for (int[] dir : directions) {
                int nr = x + dir[0];
                int nc = y + dir[1];

                //The meaning of this line is if my steps capacity at present(curr[3]) is more than what someone else would have
                //in the past while crossing this grid[nr][nc], I would definitely take this. Because let's say in the past John took
                //this node and made a mart that he has step capacity of 10 at that time , and now if I have step capacity
                //of 11, I may  have range defineitely more than or equal to 10, but never less than 10. But in the other case, I would
                //not dare to cross as someone already had much higher range than I would obtain with my best effort

                if (nr >= 0 && nr < r && nc >= 0 && nc < c && maxDistDrivble[nr][nc] < curr[3]) {
                    queue.offer(new int[]{nr, nc, curr[2] + 1, curr[3]});
                    //If I dared to cross via this node, I would make sure to leave my foot print by writing how much capacity I
                    //had while passing this through
                    maxDistDrivble[nr][nc] = curr[3];
                }
            }
        }
        return -1;

        //Credit to original author as per my knowledge Ashtonn77
    }

